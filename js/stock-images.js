export const DEFAULT_IMAGE = "https://images.unsplash.com/photo-1560518883-ce09059eeffa?auto=format&fit=crop&w=800&q=60";

/**
 * FNV-1a hash for better distribution across image arrays.
 */
function hashString(str) {
  let hash = 2166136261;
  for (let i = 0; i < str.length; i++) {
    hash ^= str.charCodeAt(i);
    hash = (hash * 16777619) >>> 0;
  }
  return hash;
}

/**
 * Returns a single stock image for a property type.
 * If seed is provided (e.g. property ID), selection is deterministic.
 */
export function getStockImage(type, seed) {
  const images = STOCK_IMAGES[type];
  if (!images || images.length === 0) return DEFAULT_IMAGE;
  if (seed) {
    return images[hashString(seed) % images.length];
  }
  return images[Math.floor(Math.random() * images.length)];
}

/**
 * Returns multiple unique stock images for a property type.
 * seed can be a number (direct offset) or string (hashed to offset).
 * Each property gets a unique starting offset so no two properties share the same primary image.
 */
export function getMultipleStockImages(type, count, seed) {
  const images = STOCK_IMAGES[type];
  if (!images || images.length === 0) {
    return Array(count).fill(DEFAULT_IMAGE);
  }

  let offset;
  if (typeof seed === 'number') {
    // Direct numeric offset — guarantees uniqueness when called with sequential indices
    offset = seed % images.length;
  } else if (seed) {
    offset = hashString(String(seed)) % images.length;
  } else {
    offset = Math.floor(Math.random() * images.length);
  }

  const result = [];
  for (let i = 0; i < count; i++) {
    result.push(images[(offset + i) % images.length]);
  }
  return result;
}
